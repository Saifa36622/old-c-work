char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	x;
	unsigned int	y;

	x = 0;
	y = 0;
	while (dest[x] != '\0')
	{
		x++;
	}
	while (src[y] && nb-- != 0)
		dest[x++] = src[y++];
	dest[x] = '\0';
	return (dest);
}
