#include <unistd.h>

void	ft_print_comb(void)
{
	char	x;
	char	x2;
	char	x3;

	x = '0';
	while (x <= '7')
	{
		x2 = x + 1;
		while (x2 <= '8')
		{
			x3 = x2 + 1;
			while (x3 <= '9')
			{
				write (1, &x, 1);
				write (1, &x2, 1);
				write (1, &x3, 1);
				if (x != '7' || x2 != '8' || x3 != '9')
					write (1, ", ", 2);
				x3++;
			}
			x2++;
		}
		x++;
	}
}
